function splitData(rawData) {
    if (parseInt(rawData[0].length) < 5) return;
    // console.log(rawData, 222);
    const categoryData = [];
    const values = [];
    for (var i = 0; i < rawData.length; i++) {
        categoryData.push(rawData[i][0]);
        values.push(rawData[i].slice(1));
    }
    // console.log(categoryData, 111);
    return {
        categoryData: categoryData,
        values: values
    };
}

function klineData(type: '1d' | '3d' | '7d', id: string) {
    let dayData = id === '1' ? klineDataJson1.data : klineDataJson2.data;
    let oneDayData = dayData.slice(0, 24);
    let threeDayData = dayData.slice(0, 72);
    let sevenDayData = dayData;
    switch (type) {
        case '1d':
            return splitData(oneDayData);
        case '3d':
            return splitData(threeDayData);
        case '7d':
            return splitData(sevenDayData);
        default:
            return splitData(oneDayData);
    }
}

